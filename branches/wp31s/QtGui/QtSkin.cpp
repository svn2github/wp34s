	int yIndex=theAttributes.index("y");
	int sizeIndex=theAttributes.index("size");
	int stretchIndex=theAttributes.index("stretch");
	int extraIndex=theAttributes.index("extra");
	int shiftIndex=theAttributes.index("shift");

